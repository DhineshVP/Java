class OperatorExample2{
	public static void main(String[] args){
	int a = 10;
	int b = 10;
	int c = 10;
	int d = -10;
	boolean e = true;
	boolean f = false;
	System.out.println(a++ + ++a);
	System.out.println(b++ + b++);
	System.out.println(~c);
	System.out.println(~d);
	System.out.println(!e);
	System.out.println(!f);
}
}